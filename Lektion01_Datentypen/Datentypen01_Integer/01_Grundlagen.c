int main() {
    // Definition und Initialisierung
    int a;
    int b;
    int c, d;
    int u, v, w, x, y, z;

    a = 1;
    b = 2;
    c = 3;
    d = 4;

    // Rechnen mit Integer
    u = a + b; // u = 3
    v = u / (2 * 2); // v = 0
    w = (a + b) * v; // 0
    x = c++; // x = 3
    y = d + (b * 3); // 10
    z = a + b + c + d; // 11
}